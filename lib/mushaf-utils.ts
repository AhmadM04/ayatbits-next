export const TOTAL_MUSHAF_PAGES = 604;

export function getJuzForPage(pageNumber: number): number {
  for (const [juz, { startPage, endPage }] of Object.entries(JUZ_PAGE_MAPPING)) {
    if (pageNumber >= startPage && pageNumber <= endPage) {
      return parseInt(juz);
    }
  }
  return 1;
}

export function getStartPageForJuz(juzNumber: number): number {
  return JUZ_PAGE_MAPPING[juzNumber]?.startPage || 1;
}

// Convert Western numerals to Arabic numerals
export function toArabicNumerals(num: number): string {
  const arabicNumerals = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];
  return num.toString().split('').map(digit => arabicNumerals[parseInt(digit)] || digit).join('');
}

